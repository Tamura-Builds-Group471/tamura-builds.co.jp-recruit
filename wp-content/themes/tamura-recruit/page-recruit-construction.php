                });

                // Note: The SDK usage might differ slightly in browser, 
                // but following the provided guidelines for generateContent
                const result = await model.generateContent({
                    contents: [{
                        parts: [{
                            text: prompt
                        }]
                    }],
                    config: {
                        imageConfig: {
                            aspectRatio: "16:9"
                        }
                    }
                });

                let imageUrl = null;
                for (const part of result.response.candidates[0].content.parts) {
                    if (part.inlineData) {
                        imageUrl = `data:image/png;base64,${part.inlineData.data}`;
                        break;
                    }
                }

                if (imageUrl) {
                    container.innerHTML = `<img src="${imageUrl}" class="h-full w-full object-cover fade-in" alt="Generated Image">`;
                } else {
                    throw new Error("No image data");
                }
            } catch (error) {
                console.error("Image generation failed:", error);
                const seed = encodeURIComponent(prompt.substring(0, 20));
                container.innerHTML = `<img src="https://picsum.photos/seed/${seed}/1200/675" class="h-full w-full object-cover fade-in" alt="Fallback Image">`;
            }
            container.classList.remove('img-placeholder');
        }

